#include "../includes/libft.h"

void	ft_lstadd_back(t_list **alst, t_list *tmp)
{
	t_list	*ptr;

	if (alst)
	{
		if (*alst == NULL)
			*alst = tmp;
		else
		{
			ptr = ft_lstlast(*alst);
			ptr->next = tmp;
		}	
	}
}
